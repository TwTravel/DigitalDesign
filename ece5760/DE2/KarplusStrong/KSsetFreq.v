//state machine start up	
wire reset; 

//pluck the string
wire pluck ;
reg last_pluck;
reg [11:0] pluck_count;

// state variable 0=reset, 1=readinput,
// 2=readoutput, 3=writeinput, 4=write 5=updatepointers, 
// 9=stop
reg [3:0] state ;
reg last_clk ; //oneshot gen

// constant for gain
wire [17:0] gain ;
		
//pointers into the shift register
//4096 at 48kHz imples 12 Hz
reg [11:0] ptr_in, ptr_out; 

//memory control
reg we; //write enable--active high
wire [17:0] sr_data; 
reg [17:0]  write_data;
reg [11:0] addr_reg;

//data registers for arithmetic
reg [17:0] out, in_data, out_data ;
wire [17:0] new_out;

//random number generator
wire x_low_bit ; // random number gen low-order bit
reg [30:0] x_rand ;//  rand number

// reset control
assign reset = ~KEY[0];

// pluck control
assign pluck = ~KEY[3] ;

//generate a random number at audio rate
// --AUD_DACLRCK toggles once per left/right pair
// --so it is the start signal for a random number update
// --at audio sample rate
//right-most bit for rand number shift regs
assign x_low_bit = x_rand[27] ^ x_rand[30];
//your basic XOR random # gen
always @ (posedge AUD_DACLRCK)
begin
	if (reset)
	begin
		x_rand <= 31'h55555555;
	end
	else begin
		x_rand <= {x_rand[29:0], x_low_bit} ;
	end
end

//when user pushes a button transfer rand number to circular buffer
//treat each bit of rand register as +/-1, 18-bit, 2'comp
//when loading to circ buffer
//shift buffer, apply filter, update indexes	
//once per audio clock tick
assign LEDG = state;
assign LEDR = ptr_out;
assign gain = 18'h0_7EB8 ; //0.495

//Run the state machine FAST so that it completes in one 
//audio cycle
always @ (posedge CLOCK_50)
begin
	if (reset)
	begin
		ptr_out <= 12'h1 ; // beginning of shift register
		ptr_in <= 12'h0 ;
		we <= 1'h0 ; 
		state <= 4'd9 ; //turn off the update state machine	
		last_clk <= 1'h1;
	end
	
	else begin
		case (state)
	
			1: 
			begin
				// set up read ptr_out data
				addr_reg <= ptr_out;
				we <= 1'h0;
				state <= 4'd2;
			end
	
			2: 
			begin
				//get ptr_out data
				out_data <= sr_data;
				// set up read ptr_in data
				addr_reg <= ptr_in;
				we <= 1'h0;
				state <= 4'd3;
			end
			
			3: 
			begin
				//get prt_in data
				in_data <= sr_data;
				state <= 4'd4 ;
			end
			
			4:
			begin
				//write ptr_in data:  
				// -- can be either computed feedback, or noise from pluck
				out <= new_out;
				addr_reg <= ptr_in;
				we <= 1'h1 ;
				// feedback or new pluck
				if (pluck)
				begin 
					// is this a new pluck?
					if (last_pluck==0)
					begin
						// if so, reset the count
						pluck_count <= 12'd0;
						last_pluck <= 1'd1;
					end
					// have the correct number of random numbers been loaded?
					else if (pluck_count<note)
					begin
						//if less, load +/-1 into memory
						pluck_count <= pluck_count + 12'd1 ;
						// shift in random number	
						if (x_rand[30]) // load 1
							write_data <= 18'h1_0000 ; 
						else //load -1
							write_data <= 18'h3_0000 ;
					end
					//update feedback if not actually loading random numbers
					else 
						//slow human holds button down, but feedback is still necessary
						write_data <= new_out;
				end
				else begin // update feedback if pluck button is not pushed
					last_pluck = 1'h0;
					write_data <= new_out;
				end
				state <= 4'd5;
			end
			
			5: 
			begin
				we <= 0;
				//update 2 ptrs
				if (ptr_in == note)
					ptr_in <= 12'h0;
				else
					ptr_in <= ptr_in + 12'h1 ;
				
				if (ptr_out == note)
					ptr_out <= 12'h0;
				else
					ptr_out <= ptr_out + 12'h1 ;
				
				//next state is end state
				state <= 4'd9;
			end
			
			9:
			begin
				if (AUD_DACLRCK && last_clk)
				begin
					state <= 4'd1 ;
					last_clk <= 1'h0 ;
				end
				else if (~AUD_DACLRCK)
				begin
					last_clk <= 1'h1 ;
					//state <= 4'd9; //the end
				end	
				//else begin
				//	state <= state;
				//	last_clk <= last_clk;
				//end
			end
			
		endcase
	end
end	

//make the shift register
ram_infer KS(sr_data, addr_reg, write_data, we, CLOCK_50);	

//make a multiplier and compute gain*(in+out)
signed_mult gainfactor(new_out, gain, (out_data + in_data));

endmodule

